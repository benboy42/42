#include "libft.h"

int		ft_atoi(char *str)
{
	int i;
	long nb;

	nb = 0;
	i = 0;
	while (str[i] == '\t' || str[i] == '\v' || str[i] == '\n' ||
			str[i] == '\r' || str[i] == '\f' || str[i] == ' ')
		i++;
	while (str[i] && str[i] <= '9' && str[i] >= '0')
	{
		nb = nb * 10 + str[i] - 48;
		i++;
	}
	return (nb);
}
