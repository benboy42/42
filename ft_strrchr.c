#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	int i;
	int j;

	i = 0;
	j = -1;
	while (s[i])
	{
		if (s[i] == c)
			j = i;
		i++;
	}
	if (j == -1)
		return (0);
	return (s + j);
}
